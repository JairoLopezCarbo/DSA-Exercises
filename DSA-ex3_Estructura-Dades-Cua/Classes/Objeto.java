package Classes;

public class Objeto {
    public String nombre;
    String tipo;
    String descripcion;
    int valor;
    int coste;

    public Objeto (String nombre, String tipo, String descripcion, int valor, int coste) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.descripcion = descripcion;
        this.valor = valor;
        this.coste = coste;
    }
}

